/**
 * t() — translation helper
 * Usage: t('greeting', 'sw-KE') → 'Habari'
 * Falls back to English if key or language is missing.
 */
export function t(key, lang = 'en-KE') {
  const langMap = translations[lang] || translations['en-KE'];
  return langMap[key] || translations['en-KE'][key] || key;
}

/**
 * formatTranslation() — replace {week} style placeholders
 * Usage: formatTranslation(t('viewWeekGuide', 'sw-KE'), { week: 24 })
 */
export function formatTranslation(template, variables = {}) {
  return Object.entries(variables).reduce(
    (str, [key, val]) => str.replace(new RegExp(`{${key}}`, 'g'), val),
    template
  );
}
